class Biblioteca{

    public Biblioteca() {
        libros = mockLibros;
    }
    
    Libro[] mockLibros = {
        new Libro("","","", "", 0),
        new Libro("","","", "", 0),
        new Libro("","","", "", 0),
        new Libro("","","", "", 0)
    };

    private Libro libros[];
    private int contador = 0;

    public Libro[] getLibros() {
        return libros;
    }

    public int getContador() {
        return contador;
    }

    public void setContador(int contador) {
        this.contador = contador;
    }

    public void setLibros(Libro[] libros) {
        this.libros = libros;
    }

    public void getLibrosInfo() {
        System.out.println("Libro disponibles:");
        for(int i = 0; i < libros.length; i++){
            System.out.println(
                    "Titulo: "+libros[i].getTitulo().toUpperCase()+"\n"+
                    "ISBN: "+libros[i].getIsbn()+"\n"+
                    "Autor: "+libros[i].getAutor()+"\n"+
                    "Precio: "+libros[i].getPrecio()+"\n"+
                    "Existencia: "+libros[i].getEjemplaresExistentes()+"\n"+
                    "_____________________________________________"
            );
        }
    }
    
    public String getLibroInfo(String isbn, Component component){
        Libro libro = searchBook(isbn);
        
        if(libro == null){
            JOptionPane.showMessageDialog(component, "Libro no encontrado. Introdusca un ISBN valido.");
            return "";
        }
        
        return "Titulo: "+libro.getTitulo().toUpperCase()+"\n"+
        "ISBN: "+libro.getIsbn()+"\n"+
        "Autor: "+libro.getAutor()+"\n"+
        "Precio: "+libro.getPrecio()+"\n"+
        "Existencia: "+libro.getEjemplaresExistentes()+"\n"+
        "____________________________";
    }

    public void agregarLibro(String isbn, Component component){
        Libro libro = searchBook(isbn);
        if(libro == null){
            JOptionPane.showMessageDialog(component, "Libro no encontrado. Favor de introducir bien el ISBN");
            return;
        }
        libro.setEjemplaresExistentes(libro.getEjemplaresExistentes()+1);
        JOptionPane.showMessageDialog(component, "Se ha agregado un libro al inventario. Titulo: "+libro.getTitulo());
    }

    public void venderLibro(String isbn, int numeroVentas, Component component){
        Libro libro = searchBook(isbn);
        if(libro == null){
            JOptionPane.showMessageDialog(component, "Libro no encontrado. Favor de introducir bien el ISBN");
            return;
        }
        if((libro.getEjemplaresExistentes()-numeroVentas) >= 0){
            libro.setEjemplaresExistentes(libro.getEjemplaresExistentes()-numeroVentas);
            JOptionPane.showMessageDialog(component, "Libro "+libro.getTitulo()+" vendido correctamente.");
        }else {
            JOptionPane.showMessageDialog(component, "No quedan libros en el inventario");
        }
    }

    public Libro searchBook(String isbn){
        Libro libro = null;
        for(Libro searchLibro: libros){
            if(searchLibro.getIsbn().equals(isbn)) {
                libro = searchLibro;
                break;
            }
        }
        return libro;
    }
    
    public void agregarColeccion(Libro libro, int position, Component component){
        libros[position] = libro;
        JOptionPane.showMessageDialog(component, libro.getTitulo()+" guardado correctamente. ISBN: "+libro.getIsbn());
    }

}

class Libro {
    public Libro(String isbn, String autor, String titulo, String precio, int ejemplaresExistentes){
        this.isbn = isbn;
        this.autor = autor;
        this.titulo = titulo;
        this.precio = precio;
        this.ejemplaresExistentes = ejemplaresExistentes;
    }
    private String isbn;
    private String autor;
    private String titulo;
    private String precio;
    private int ejemplaresExistentes;

    public String getIsbn() {
        return isbn;
    }

    public String getAutor() {
        return autor;
    }

    public String getTitulo() {
        return titulo;
    }

    public String getPrecio() {
        return precio;
    }

    public int getEjemplaresExistentes() {
        return ejemplaresExistentes;
    }

    public void setEjemplaresExistentes(int ejemplaresExistentes) {
        this.ejemplaresExistentes = ejemplaresExistentes;
    }
}
