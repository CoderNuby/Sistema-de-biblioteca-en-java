    private void btnAgregarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarLibroActionPerformed
        // TODO add your handling code here:
        
        if(biblioteca.getContador() < 4){
            String isbn = tfIsbn.getText();
            String autor = tfAutor.getText();
            String titulo = tfTitulo.getText();
            String precio = tfPrecio.getText();
            int ejemplaresExistentes = Integer.parseInt(tfNoEjemplares.getText());


            Libro libro = new Libro(isbn, autor, titulo, precio, ejemplaresExistentes);
            
            biblioteca.agregarColeccion(libro, biblioteca.getContador(), btnAgregarLibro);
            
            
            int num = biblioteca.getContador() + 1;
            biblioteca.setContador(num);
        }else{
            JOptionPane.showMessageDialog(btnAgregarLibro, "Capasidad maxima de lotes");
        }
        
        tfIsbn.setText("");
        tfAutor.setText("");
        tfTitulo.setText("");
        tfPrecio.setText("");
        tfNoEjemplares.setText("");
    }//GEN-LAST:event_btnAgregarLibroActionPerformed

